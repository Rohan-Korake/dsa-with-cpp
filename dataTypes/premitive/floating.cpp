#include <iostream>
using namespace std;

int main() {
  // Write f or F to define this is float value
  // It required 4 Bytes of data in memory
  float PI = 3.14f;

  cout << "Value of floating variable is " << PI << endl;
  cout << "Size of floating datatype is " << sizeof(float) << " Bytes" << endl;

  return 0;
}